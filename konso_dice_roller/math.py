def greater_than_or_equal(left: float, right: float) -> bool:
    return left >= right


def less_than_or_equal(left: float, right: float) -> bool:
    return left <= right


def greater_than(left: float, right: float) -> bool:
    return left > right


def less_than(left: float, right: float) -> bool:
    return left < right


def equal(left: float, right: float) -> bool:
    return left == right
